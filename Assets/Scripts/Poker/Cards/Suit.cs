namespace Poker.Cards {
    
    /// <summary> Represents a suit from the standard 52 card deck. </summary>
    public enum Suit {
        
        /// <summary> The heart. One of two red suits. </summary>
        Heart,

        /// <summary> The diamond. One of two red suits. </summary>
        Diamond,

        /// <summary> The spade. One of two black suits. </summary>
        Spade,

        /// <summary> The club. One of two black suits. </summary>
        Club
    }
}